package com.kingsrook.qbits.sftpdataintegration.metadata;


import com.kingsrook.qbits.sftpdataintegration.model.SFTPExportConfig;
import com.kingsrook.qqq.backend.core.exceptions.QException;
import com.kingsrook.qqq.backend.core.model.dashboard.widgets.WidgetType;
import com.kingsrook.qqq.backend.core.model.metadata.MetaDataProducer;
import com.kingsrook.qqq.backend.core.model.metadata.QInstance;
import com.kingsrook.qqq.backend.core.model.metadata.code.QCodeReference;
import com.kingsrook.qqq.backend.core.model.metadata.dashboard.QWidgetMetaData;
import com.kingsrook.qqq.backend.core.model.savedreports.ReportValuesDynamicFormWidgetRenderer;


/*******************************************************************************
 ** Meta Data Producer for SFTPExportConfigReportValuesWidget
 *******************************************************************************/
public class SFTPExportConfigReportValuesWidgetMetaDataProducer extends MetaDataProducer<QWidgetMetaData>
{
   public static final String NAME = "SFTPExportConfigReportValuesWidget";



   /*******************************************************************************
    **
    *******************************************************************************/
   @Override
   public QWidgetMetaData produce(QInstance qInstance) throws QException
   {
      return new QWidgetMetaData()
         .withName(NAME)
         .withType(WidgetType.DYNAMIC_FORM.getType())
         .withIsCard(true)
         .withLabel("Variable Values")
         .withCodeReference(new QCodeReference(ReportValuesDynamicFormWidgetRenderer.class))
         .withDefaultValue("tableName", SFTPExportConfig.TABLE_NAME);
   }

}
